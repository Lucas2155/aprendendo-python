# Exercícios
# Crie funções que duplicam, triplicam e quadriplicam
# o número recebido como parâmetro.

def duplicar(numero):
    return numero * 2

def triplicar(numero):
    return numero * 3

def quadruplicar(numero):
    return numero * 4

print(duplicar(2))
print(triplicar(3))
print(quadruplicar(10))